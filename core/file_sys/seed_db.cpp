static std::array<u8, 16> hexToBin(const std::string& hex) {
    std::array<u8, 16> bytes;

    for (unsigned int i = 0; i < hex.length(); i += 2) {
        std::string byteString = hex.substr(i, 2);
        bytes[i/2] = static_cast<u8>(std::strtol(byteString.c_str(), nullptr, 16));
    }
	
    return bytes;
}
bool SeedDB::Load() {
    seeds.clear();
	initSeeds(seeds);
    const std::string path{StringFromFormat(
        "%s/seeddb.bin", FileUtil::GetUserPath(FileUtil::UserPath::SysDataDir).c_str())};
    if (!FileUtil::Exists(path)) {
        if (!FileUtil::CreateFullPath(path)) {
            LOG_ERROR(Service_FS, "Failed to create seed database");
            return false;
        }
        if (!Save()) {
            LOG_ERROR(Service_FS, "Failed to save seed database");
            return false;
        }
        return true;
    }
    FileUtil::IOFile file{path, "rb"};
    if (!file.IsOpen()) {
        LOG_ERROR(Service_FS, "Failed to open seed database");
        return false;
    }
    u32 count;
    if (!file.ReadBytes(&count, sizeof(count))) {
        LOG_ERROR(Service_FS, "Failed to read seed database count fully");
        return false;
    }
    if (!file.Seek(SEEDDB_PADDING_BYTES, SEEK_CUR)) {
        LOG_ERROR(Service_FS, "Failed to skip seed database padding");
        return false;
    }
    for (u32 i = 0; i < count; ++i) {
        Seed seed;
        if (!file.ReadBytes(&seed.title_id, sizeof(seed.title_id))) {
            LOG_ERROR(Service_FS, "Failed to read seed {} title ID", i);
            return false;
        }
        if (!file.ReadBytes(seed.data.data(), seed.data.size())) {
            LOG_ERROR(Service_FS, "Failed to read seed {} data", i);
            return false;
        }
        if (!file.ReadBytes(seed.reserved.data(), seed.reserved.size())) {
            LOG_ERROR(Service_FS, "Failed to read seed {} reserved data", i);
            return false;
        }
	//	LOG_ERROR(Service_FS, "seed dump {} {} {}", seed.title_id, binToHex(seed.data), binToHex(hexToBin(binToHex(seed.data))));
	//	LOG_ERROR(Service_FS, "seed check {}", areArraysIdentical(hexToBin(binToHex(seed.data)), seed.data));
			
        seeds[seed.title_id] = seed;
    }
    return true;
}

bool SeedDB::Save() {
    const std::string path{StringFromFormat(
        "%s/seeddb.bin", FileUtil::GetUserPath(FileUtil::UserPath::SysDataDir).c_str())};
    if (!FileUtil::CreateFullPath(path)) {
        LOG_ERROR(Service_FS, "Failed to create seed database");
        return false;
    }
    FileUtil::IOFile file{path, "wb"};
    if (!file.IsOpen()) {
        LOG_ERROR(Service_FS, "Failed to open seed database");
        return false;
    }
    u32 count{static_cast<u32>(seeds.size())};
    if (file.WriteBytes(&count, sizeof(count)) != sizeof(count)) {
        LOG_ERROR(Service_FS, "Failed to write seed database count fully");
        return false;
    }
    std::array<u8, SEEDDB_PADDING_BYTES> padding{};
    if (file.WriteBytes(padding.data(), padding.size()) != padding.size()) {
        LOG_ERROR(Service_FS, "Failed to write seed database padding fully");
        return false;
    }
    for (auto iter = seeds.begin(); iter != seeds.end(); ++iter) {
        if (file.WriteBytes(&(iter->second.title_id), sizeof(iter->second.title_id)) != sizeof(iter->second.title_id)) {
            LOG_ERROR(Service_FS, "Failed to write seed {} title ID fully", iter->first);
            return false;
        }
        if (file.WriteBytes(iter->second.data.data(), iter->second.data.size()) != iter->second.data.size()) {
            LOG_ERROR(Service_FS, "Failed to write seed {} data fully", iter->first);
            return false;
        }
        if (file.WriteBytes(iter->second.reserved.data(), iter->second.reserved.size()) != iter->second.reserved.size()) {
            LOG_ERROR(Service_FS, "Failed to write seed {} reserved data fully", iter->first);
            return false;
        }
    }
    return true;
}

void SeedDB::Add(const Seed& seed) {
    seeds[seed.title_id] = seed;
}

bool SeedDB::Delete(u64 title_id) {
    seeds.erase(title_id);
    return true;
}

std::size_t SeedDB::GetCount() const {
    return seeds.size();
}

auto SeedDB::FindSeedByTitleID(u64 title_id) const {
    return seeds.find(title_id);
}

bool AddSeed(const Seed& seed) {
    // TODO: does this skip/replace if the SeedDB contains a seed for seed.title_id?
    SeedDB db;
    if (!db.Load()) {
        LOG_ERROR(Service_FS, "Failed to load seed database");
        return false;
    }
    db.Add(seed);
    if (!db.Save()) {
        LOG_ERROR(Service_FS, "Failed to save seed database");
        return false;
    }
    return true;
}

std::optional<Seed::Data> GetSeed(u64 title_id) {
    SeedDB db;
    if (!db.Load()) {
        return std::nullopt;
    }
    const auto found_seed_iter{db.FindSeedByTitleID(title_id)};
    if (found_seed_iter != db.seeds.end()) {
        return found_seed_iter->second.data;
    }
    return std::nullopt;
}

bool DeleteSeed(u64 title_id) {
    SeedDB db;
    if (!db.Load()) {
        LOG_ERROR(Service_FS, "Failed to load seed database");
        return false;
    }
    bool found = db.Delete(title_id);
    if (found) {
        if (!db.Save()) {
            LOG_ERROR(Service_FS, "Failed to save seed database");
        }
    }
    return found;
}

u32 GetSeedCount() {
    SeedDB db;
    if (!db.Load()) {
        return 0;
    }
    return static_cast<u32>(db.GetCount());
}

static void initSeed(std::unordered_map<u64_le, Seed> &seeds, u64 title_id, std::string hexData)
{
	Seed seed;
	
	seed.title_id = title_id;
	seed.data = hexToBin(hexData);
	
	seeds[title_id] = seed;
}

